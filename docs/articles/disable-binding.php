	<h1>"disable" バインディング</h1>
	
	<h3 id="purpose">用途</h3>
	
	<p>
		<code>disable</code> バインディングは、値が <code>true</code>
		のときだけ関連付けられた DOM エレメントを使用不可能にします。
		<code>input</code> や <code>select</code>, <code>textarea</code> などの form 部品でよく使います。
	</p>
	
	<p>
		<code>disable</code> バインディングは <code>enable</code> バインディングのミラーイメージです。
		<code>disable</code> バインディングは <code>enable</code> バインディングに反転した値を設定することと全く同じ効果があります。
		詳細は <a href="enable-binding">"enable" バインディング</a> をご覧ください。
	</p>
	
